package com.opcoach.e34.tools.model;

public class CustomSchema {
    private String id = null;

    private final String cepId = null;

    public CustomSchema(String cepId, String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getCustomExtentionPointId() {
        return cepId;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        CustomSchema other = (CustomSchema) obj;
        if (id == null) {
            if (other.id != null) {
                return false;
            }
        }
        else if (!id.equals(other.id)) {
            return false;
        }
        return true;
    }

}
